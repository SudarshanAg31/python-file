#for loop:executed a block of code a fixed number of time .
#         you can iterate over a rang,string,sequence etc.

#for loop: format = for (varible_name) in range(start,end,move)
for x in range(1,11,2):
    print(x) 

#reverse for loop: format = for (varible_name) in reversed(range(start,end,move))
for x in reversed(range(1,11)):
    print(x)

#for loop for string: format = for (varible_name) in (string name)
name="sudarshan"
